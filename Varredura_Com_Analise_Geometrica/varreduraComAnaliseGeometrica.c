void varreduraComAnaliseGeometrica(int figura[TAM_MAT_FIG][TAM_MAT_FIG] ){
	int i , j;
	for(i=0 ; i<TAM_MAT_FIG ; i++){
		for(j=0 ; j<TAM_MAT_FIG ; j++){
			if(figura[i][j] == BORDA){
				while(figura[i][++j] != BORDA && j<TAM_MAT_FIG){
					figura[i][j] = COLOR_NEW;
				}
			}
		}
	}
}

void imprimeDesenho(char *nomeFigura, int desenho[TAM_MAT_FIG][TAM_MAT_FIG]){

	printf("Desenho: %s\n",nomeFigura );

	int i, j;

	for(i=0 ; i<TAM_MAT_FIG ; i++){
		for(j=0 ; j<TAM_MAT_FIG ; j++){
			if(desenho[i][j] == 1){
				printf("1 ");
			} else if(desenho[i][j] == 5){
				printf(". ");
			} else {
				printf("  ");
			}
		}
		printf("\n");
	}
	printf("\n");
}
